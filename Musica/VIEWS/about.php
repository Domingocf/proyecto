<section class="container mt-5">
  <div class="text-center mb-4">
    <h2>Sobre MusicConnect</h2>
    <p class="text-muted">Una experiencia musical social y colaborativa</p>
  </div>

  <p>
    MusicConnect nació con una idea simple pero ambiciosa: llevar la música más allá del oído y convertirla en una experiencia compartida. En lugar de limitarse a ofrecer canciones, nuestra plataforma conecta a personas con gustos similares, permitiéndoles descubrir, compartir y comentar música en un entorno seguro y accesible.
  </p>

  <p>
    La plataforma está pensada para distintos tipos de usuarios. Los <strong>administradores</strong> supervisan la comunidad y mantienen el orden; los <strong>usuarios normales</strong> pueden escuchar música, crear listas y participar activamente; y los <strong>usuarios premium</strong> disfrutan de todas las funcionalidades sin restricciones ni anuncios, accediendo a un catálogo completo y mejorado.
  </p>

  <p>
    MusicConnect combina funcionalidades técnicas modernas con un diseño pensado para todos. Entre sus características más importantes destacan:
  </p>

  <ul>
    <li><strong>Reproducción musical fluida:</strong> escucha canciones de tu catálogo favorito sin interrupciones.</li>
    <li><strong>Listas de reproducción personalizadas:</strong> crea, organiza y comparte tus listas con amigos o manténlas privadas según tu preferencia.</li>
    <li><strong>Interacción social:</strong> comenta, valora y reporta contenido, fomentando una comunidad activa y responsable.</li>
    <li><strong>Notificaciones inteligentes:</strong> recibe correos automáticos por registro, sanciones o resolución de reportes, manteniendo a todos informados.</li>
    <li><strong>Accesibilidad universal:</strong> diseño responsive, compatible con dispositivos móviles, tabletas y ordenadores, cumpliendo estándares WCAG 2.1 AA.</li>
    <li><strong>Seguridad y privacidad:</strong> protección de datos personales según RGPD, contraseñas encriptadas y control de permisos según rol.</li>
    <li><strong>Tecnología moderna:</strong> PHP 8, MySQL, HTML5, CSS3, JavaScript, Bootstrap 5, AJAX para interacciones en tiempo real y notificaciones visuales con SweetAlert 2.</li>
  </ul>

  <p>
    Cada detalle de MusicConnect fue planificado pensando en la experiencia del usuario y en la escalabilidad. Desde el diseño de la base de datos hasta la implementación de funcionalidades sociales, pasando por pruebas de usabilidad y accesibilidad, todo busca ofrecer un entorno seguro, atractivo y eficiente.
  </p>

  <p>
    Lo que hace único a MusicConnect es su enfoque social: a diferencia de otras plataformas de streaming, aquí la música no es solo un consumo individual, sino un puente para la interacción. Los usuarios pueden descubrir nuevas canciones gracias a las listas de otros, dejar comentarios, valorar música y participar en discusiones que enriquecen la experiencia.
  </p>

  <p>
    MusicConnect se desarrolla siguiendo fases bien definidas: análisis, diseño, implementación, integración, pruebas y despliegue. Contamos con un equipo dedicado, recursos tecnológicos modernos y un plan de mantenimiento que garantiza la continuidad del servicio y la mejora constante.
  </p>

  <p>
    Nuestra visión es clara: ofrecer una plataforma musical accesible, segura, personalizada y social, donde la tecnología y la comunidad se encuentren para hacer de cada escucha una experiencia compartida y memorable.
  </p>

  <p class="text-center mt-4">
    <em>Descubre, comparte y conecta a través de la música con MusicConnect.</em>
  </p>
